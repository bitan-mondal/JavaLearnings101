package tutorials.basicConcepts;

public class VariableDemo {
	public static void main(String args[])
	{
		int num = 10; 
		System.out.println(num);
		
		float percent = 5.5f;
		
		System.out.println(percent);
		
		double d = 5;
		
		System.out.println(d);
		
		int k = (int)5.5;
		
		System.out.println(k);
	}

}
